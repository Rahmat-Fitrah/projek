        axes2[1].axvline(x=0, color='red', linestyle='--')
        axes2[1].set_xlabel('Residual')
        axes2[1].set_ylabel('Frekuensi')
        axes2[1].set_title(f'Residual Testing (Mean = {residuals_test.mean():.0f})')

