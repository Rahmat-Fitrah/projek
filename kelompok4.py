        ax3.barh(names, values)
        ax3.set_xlabel('Pengaruh (Standar Deviasi Prediksi)')
        ax3.set_title('Pentingnya Fitur dalam Model KNN')
        plt.tight_layout()
        st.pyplot(fig3)

